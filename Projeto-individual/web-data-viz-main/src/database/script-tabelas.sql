        WHEN r.fk_pokemon IS NOT NULL THEN 1
        ELSE 0
    END AS registrado
FROM pokemon AS p
LEFT JOIN registro AS r
    ON p.id = r.fk_pokemon
    AND r.fk_usuario = 2;

